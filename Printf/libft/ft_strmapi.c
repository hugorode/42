#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*new;

	if (!s)
		return (0);
	i = 0;
	new = malloc ((ft_strlen((char *)s) + 1) * sizeof(char));
	if (new == 0)
		return (0);
	while (s[i])
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
